defmodule VectorHelper do
  def from_hex(s),
    do:
      s
      |> String.split(~r/\s+/i, trim: true)
      |> Enum.map(fn n -> Integer.parse(n, 16) end)
      |> Enum.reduce(<<>>, fn {i, ""}, acc -> acc <> <<i>> end)

  def tag_from_bin(b), do: conv_chars(b, <<>>)
  defp conv_chars(<<>>, acc), do: acc

  defp conv_chars(<<c, rest::binary>>, acc) do
    conv = c |> Integer.to_string(16) |> String.downcase()

    chars =
      case byte_size(conv) do
        2 -> conv
        1 -> "0" <> conv
        0 -> "00"
      end

    conv_chars(rest, acc <> chars)
  end
end

